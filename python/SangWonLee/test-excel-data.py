tmp = 온라인_햇살론_일부_고객_대출_정보

# 데이터가 신뢰할 수 없기 때문에 밀어버리고 신뢰할 수 있는 정보를 바탕으로 정보를 구축해야함
# -999랑 999999991이 들어간 부분이 정확이 무엇을 의미하는지 모르기 때문에 없애버리자
