<template lang="">
    <div v-if="email !==null">
        <h3>{{email}}계정으로 로그인 하셨습니다!</h3>
    </div>
</template>
<script>
import axios from 'axios';

export default {
    data() {
        return {
            email: null,
        }
    },
    mounted() {
        let AccountId = localStorage.getItem("loginUserInfo")
        axios.post('http://localhost:7778/bmp-account/find-account-info',
        {AccountId: AccountId})
        .then((res)=> {
            this.email = res.data
        })
    },
}
</script>
