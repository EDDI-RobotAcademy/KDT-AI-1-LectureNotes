# tmp의 각 행에 대하여 값이 -999, 999999991이 있는지 확인하고 있으면 TRUE, 없으면 FALSE 반환함
# 근데 ~ 연산자로 인해 각각 값이 바뀌고 -999, 999999991이 없는 행만 선택함

# 실제 대출한 금액, 신용 점수, 대출 상품, 직위, 직군, 미상환 금액, 미상환 기관 개수
